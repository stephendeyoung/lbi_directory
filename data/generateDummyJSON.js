var mongodb = require('mongodb');
var countriesData = require('./countries');
var citiesData = require('./lbiCities');

// Replace these with your custom settings if required
var HOST = '127.0.0.1';
var PORT = 27017;
var dbUser = undefined;
var dbPassword = undefined;

var server = new mongodb.Server(HOST, PORT, {});
new mongodb.Db('admin', server).open(function (error, client) {
    if (error) {
        throw error;
    } else {
        if (dbUser) {
            client.authenticate(dbUser, dbPassword, function(err, p_client) {
                if (err) {
                    throw err;
                } else {
                    addCities(client, addCountryMapData);
                }
            });
        } else {
            addCities(client, addCountryMapData);
        }
    }
});

var addCities = function(client, callback) {
    var newDb = client.db('cities');
    for (var x = 0, ln1 = cities.length; x < ln1; x += 1) {
        var collection = new mongodb.Collection(newDb, cities[x]);
        var docs = [];
        var staff = [];
        var entries = Math.floor(Math.random() * 600) + 50;

        for (var i = 0; i < entries; i += 1) {
            var entry = {};
            var department = Math.floor(Math.random() * (departments.length - 1)) + 0;
            var image = Math.floor(Math.random() * 11) + 1;
            var job = Math.floor(Math.random() * (title.length - 1)) + 0;
            var surname = Math.floor(Math.random() * (sn.length - 1)) + 0;
            var firstName = Math.floor(Math.random() * (givenName.length - 1)) + 0;
            entry.department = departments[department];
            entry.l = cities[x];
            entry.title = title[job];
            entry.sn = sn[surname];
            entry.givenName = givenName[firstName];
            staff.push(givenName[firstName] + ' ' + sn[surname]);
            if (Math.floor(Math.random() * 10) + 1 > 8) {
                entry.manager = staff[Math.floor(Math.random() * staff.length) + 0];
            }
            if (image < 11) {
                entry.photo = 'image' + image + '.jpg';
            }
            docs.push(entry);
        }
        collection.insert(docs, function(err, objects) {
            if (err) {
                console.warn(err.message);
            } else {
                console.log(cities[x] + ' collection created');
            }
        });
    }
    callback(client, addCityMapData);
};

addCountryMapData = function(client, callback) {
    var mapDb = client.db('mapData');
    mapDb.createCollection('countries', function(error, collection) {
        if (error) {
            throw error;
        } else {
            collection.insert(countriesData.features, function(err, objects) {
                if (err) {
                    console.warn(err.message);
                } else {
                    console.log('countries collection created');
                    callback(client, mapDb);
                }
            });
        }
    });
};

addCityMapData = function(client, db) {
    db.createCollection('cities', function(error, collection) {
        if (error) {
            throw error;
        } else {
            collection.insert(citiesData.features, function(err, objects) {
                if (err) {
                    console.warn(err.message);
                } else {
                    console.log('cities collection created');
                    client.close();
                }
            });
        }
    });
};



